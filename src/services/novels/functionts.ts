export async function getNovels(params: GetNovelsParams) {
  return {
    pagination: {
      limit: params.limit,
      page: params.page || 1,
      pages: 1,
      total: params.limit,
    },
    novels: novels.splice(0, params.limit),
  };
}

export async function getNovelById(params: GetNovelByIdParams) {
  return novels.find((novel) => novel.id === params.id);
}

export async function getNovelChapters(params: GetNovelChaptersParams) {
  return novelChapters.find((novel) => novel.id === params.id)?.chapters || [];
}
